package week1.day2;

public class LearnDatatype {
	public static void main(String[] args) {
		// syntax : type variableName --> Declaration
		// variableName = value ; ---> Initialization
		byte age;
		age = 29;
		short num;
		num = 10000;
		int profit = 456789124;
		long phoneNo = 9807654321l;
		float decimal = 123.373883f;
		double decimalOne = 123.83898873837387;
		// 137.0.7151.70 ---> user defined datatype
		String browserVersion = "137.0.7151.70";
		char literal = 'A';
		boolean verification = true;
		System.out.println(age);
		System.out.println(num);
		System.out.println(profit);
		System.out.println(phoneNo);
		System.out.println(decimal);
		System.out.println(decimalOne);
		System.out.println(browserVersion);
		System.out.println(literal);
		System.out.println(verification);
	}

}
