package week1.day2;

public class LearnIncrementDecrement {

	public static void main(String[] args) {
    int i = 1;
    int j =10;
    // Increment--> value will added--> ++
    // Decrement --> value will get subracted --> --
    // pre--> the incr/decre action is perfomed 1st, value is retrieved then
    //post --> value is retrieved 1st, then the incr/decre action is perfomed
    System.out.println(++i);//2
    System.out.println(i++);//2
    System.out.println(i);//3
    System.out.println(--j);
 	}

}
